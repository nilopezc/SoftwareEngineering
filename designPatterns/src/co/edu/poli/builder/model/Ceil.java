package co.edu.poli.builder.model;

public class Ceil {
	
	private double ancho;
	private double largo;
	
	public Ceil(double ancho, double largo) {
		super();
		this.ancho = ancho;
		this.largo = largo;
	}

	@Override
	public String toString() {
		return "Ceil [ancho=" + ancho + ", largo=" + largo + "]";
	}
	
}
